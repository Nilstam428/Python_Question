# Q write a lambda function to add two numbers ?
add=lambda x,y:x+y
subtraction=lambda x,y:x-y
Division=lambda x,y:x/y
Multiplication=lambda x,y:x*y
Modula=lambda x,y:x%y
a=int(input("Enter first number:- "))
b=int(input("Enter second value:-"))
print("Additation:-",add(a,b))
print("Substract:-",subtraction(a,b))
print("Multiply:-",Multiplication(a,b))
print("Devide:-",Division(a,b))
print("Modules:-",Modula(a,b))